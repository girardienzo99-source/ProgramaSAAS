CREATE TABLE IF NOT EXISTS public.supermarket_purchase_receipts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  order_id UUID REFERENCES public.supermarket_purchase_orders(id) ON DELETE CASCADE NOT NULL,
  shipment_id UUID REFERENCES public.supermarket_supplier_shipments(id) ON DELETE SET NULL,
  receipt_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  user_id UUID NOT NULL,
  idempotency_key VARCHAR(128) NOT NULL,
  received_on DATE DEFAULT CURRENT_DATE NOT NULL,
  delivery_note_number VARCHAR(80),
  accepted_quantity NUMERIC(14, 3) NOT NULL CHECK (accepted_quantity >= 0),
  rejected_quantity NUMERIC(14, 3) NOT NULL CHECK (rejected_quantity >= 0),
  remaining_quantity NUMERIC(14, 3) NOT NULL CHECK (remaining_quantity >= 0),
  reconciliation_status VARCHAR(30) NOT NULL
    CHECK (reconciliation_status IN ('matched', 'partial', 'rejected_items', 'document_mismatch', 'without_asn')),
  order_status VARCHAR(20) NOT NULL CHECK (order_status IN ('partially_received', 'received')),
  notes TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, receipt_number),
  UNIQUE(company_id, idempotency_key)
);

CREATE TABLE IF NOT EXISTS public.supermarket_purchase_receipt_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  receipt_id UUID REFERENCES public.supermarket_purchase_receipts(id) ON DELETE CASCADE NOT NULL,
  order_line_id UUID REFERENCES public.supermarket_purchase_order_lines(id) ON DELETE RESTRICT NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  ordered_quantity NUMERIC(14, 3) NOT NULL CHECK (ordered_quantity > 0),
  previously_received NUMERIC(14, 3) NOT NULL CHECK (previously_received >= 0),
  accepted_quantity NUMERIC(14, 3) NOT NULL CHECK (accepted_quantity >= 0),
  rejected_quantity NUMERIC(14, 3) NOT NULL CHECK (rejected_quantity >= 0),
  remaining_quantity NUMERIC(14, 3) NOT NULL CHECK (remaining_quantity >= 0),
  unit_cost NUMERIC(14, 4) NOT NULL CHECK (unit_cost >= 0),
  lot_code VARCHAR(100),
  expiration_date DATE,
  UNIQUE(receipt_id, order_line_id)
);

CREATE TABLE IF NOT EXISTS public.supermarket_edi_outbox (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  event_type VARCHAR(10) NOT NULL CHECK (event_type IN ('DESADV', 'RECADV')),
  standard VARCHAR(20) DEFAULT 'EANCOM_D01B' NOT NULL,
  shipment_id UUID REFERENCES public.supermarket_supplier_shipments(id) ON DELETE CASCADE,
  receipt_id UUID REFERENCES public.supermarket_purchase_receipts(id) ON DELETE CASCADE,
  idempotency_key VARCHAR(180) NOT NULL,
  payload JSONB NOT NULL,
  status VARCHAR(20) DEFAULT 'pending' NOT NULL CHECK (status IN ('pending', 'processing', 'sent', 'failed')),
  retry_count INTEGER DEFAULT 0 NOT NULL CHECK (retry_count BETWEEN 0 AND 100),
  last_error TEXT,
  available_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  sent_at TIMESTAMP WITH TIME ZONE,
  UNIQUE(company_id, idempotency_key),
  CHECK ((shipment_id IS NOT NULL)::INTEGER + (receipt_id IS NOT NULL)::INTEGER = 1)
);

