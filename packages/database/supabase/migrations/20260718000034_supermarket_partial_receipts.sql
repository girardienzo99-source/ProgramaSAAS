CREATE OR REPLACE FUNCTION public.supermarket_queue_shipment_edi()
RETURNS TRIGGER
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public, pg_catalog
AS $$
BEGIN
  INSERT INTO public.supermarket_edi_outbox(
    company_id, branch_id, event_type, shipment_id, idempotency_key, payload
  ) VALUES (
    NEW.company_id, NEW.branch_id, 'DESADV', NEW.id,
    'DESADV:' || NEW.id::TEXT || ':' || floor(extract(epoch FROM NEW.updated_at) * 1000000)::BIGINT::TEXT,
    jsonb_build_object(
      'messageType', 'DESADV', 'shipmentId', NEW.id, 'orderId', NEW.order_id,
      'supplierId', NEW.supplier_id, 'dispatchNumber', NEW.dispatch_number,
      'carrier', NEW.carrier, 'trackingNumber', NEW.tracking_number,
      'shippedOn', NEW.shipped_on, 'estimatedArrival', NEW.estimated_arrival,
      'packageCount', NEW.package_count, 'palletCount', NEW.pallet_count,
      'status', NEW.status, 'generatedAt', timezone('utc'::text, now())
    )
  ) ON CONFLICT (company_id, idempotency_key) DO NOTHING;
  RETURN NEW;
END;
$$;

