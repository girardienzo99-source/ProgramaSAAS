CREATE OR REPLACE FUNCTION public.gastronomy_list_menu(p_company_id UUID)
RETURNS TABLE(
  id UUID, name TEXT, description TEXT, category TEXT, sku TEXT, price NUMERIC,
  cost NUMERIC, stock NUMERIC, min_stock NUMERIC, vat_rate NUMERIC,
  active BOOLEAN, image_url TEXT, created_at TIMESTAMP WITH TIME ZONE
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    p.id,
    p.name::TEXT,
    COALESCE(p.description, '')::TEXT,
    g.category::TEXT,
    COALESCE(p.sku, '')::TEXT,
    p.price,
    p.cost,
    COALESCE((SELECT SUM(s.quantity) FROM public.stock s WHERE s.company_id = p_company_id AND s.product_id = p.id), 0),
    COALESCE((SELECT MAX(s.min_stock) FROM public.stock s WHERE s.company_id = p_company_id AND s.product_id = p.id), 0),
    p.vat_rate,
    g.active,
    p.image_url,
    p.created_at
  FROM public.gastronomy_menu_items g
  JOIN public.products p ON p.id = g.product_id AND p.company_id = g.company_id
  WHERE g.company_id = p_company_id
  ORDER BY g.sort_order, p.name;
$$;

CREATE OR REPLACE FUNCTION public.gastronomy_save_menu_item(
  p_company_id UUID,
  p_user_id UUID,
  p_branch_id UUID,
  p_product_id UUID,
  p_name TEXT,
  p_description TEXT,
  p_category TEXT,
  p_sku TEXT,
  p_price NUMERIC,
  p_cost NUMERIC,
  p_stock NUMERIC,
  p_min_stock NUMERIC,
  p_vat_rate NUMERIC,
  p_active BOOLEAN,
  p_image_url TEXT
)
RETURNS UUID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_catalog
AS $$
DECLARE
  v_product_id UUID := p_product_id;
  v_stock_id UUID;
  v_previous_stock NUMERIC := 0;
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM public.companies c
    JOIN public.business_types bt ON bt.id = c.business_type_id
    WHERE c.id = p_company_id AND c.status = 'active' AND bt.code = 'gastronomy'
  ) THEN
    RAISE EXCEPTION 'INVALID_GASTRONOMY_COMPANY';
  END IF;
  IF NOT EXISTS (SELECT 1 FROM public.branches b WHERE b.id = p_branch_id AND b.company_id = p_company_id) THEN
    RAISE EXCEPTION 'BRANCH_COMPANY_MISMATCH';
  END IF;

  IF v_product_id IS NULL THEN
    INSERT INTO public.products(company_id, name, description, sku, price, cost, vat_rate, image_url)
    VALUES (p_company_id, p_name, p_description, p_sku, p_price, p_cost, p_vat_rate, p_image_url)
    RETURNING id INTO v_product_id;
  ELSE
    UPDATE public.products SET
      name = p_name,
      description = p_description,
      sku = p_sku,
      price = p_price,
      cost = p_cost,
      vat_rate = p_vat_rate,
      image_url = p_image_url
    WHERE id = v_product_id AND company_id = p_company_id;
    IF NOT FOUND THEN RAISE EXCEPTION 'PRODUCT_NOT_FOUND'; END IF;
  END IF;

  INSERT INTO public.gastronomy_menu_items(product_id, company_id, category, active)
  VALUES (v_product_id, p_company_id, p_category, p_active)
  ON CONFLICT (product_id) DO UPDATE SET
    category = EXCLUDED.category,
    active = EXCLUDED.active,
    updated_at = timezone('utc'::text, now());

  SELECT s.id, s.quantity INTO v_stock_id, v_previous_stock
  FROM public.stock s
  WHERE s.company_id = p_company_id AND s.branch_id = p_branch_id AND s.product_id = v_product_id;

  IF v_stock_id IS NULL THEN
    INSERT INTO public.stock(company_id, branch_id, product_id, quantity, min_stock)
    VALUES (p_company_id, p_branch_id, v_product_id, p_stock, p_min_stock)
    RETURNING id INTO v_stock_id;
  ELSE
    UPDATE public.stock SET quantity = p_stock, min_stock = p_min_stock WHERE id = v_stock_id;
  END IF;

  IF p_stock IS DISTINCT FROM v_previous_stock THEN
    INSERT INTO public.stock_movements(company_id, stock_id, user_id, quantity, type, notes)
    VALUES (p_company_id, v_stock_id, p_user_id, p_stock - v_previous_stock, 'catalog_adjustment', 'Gastronomy menu editor');
  END IF;
  RETURN v_product_id;
EXCEPTION
  WHEN unique_violation THEN RAISE EXCEPTION 'SKU_ALREADY_EXISTS';
END;
$$;

