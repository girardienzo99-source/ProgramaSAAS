-- Gastronomy domain: catalog, recipes, dining room and orders.

WITH permission_definitions(name, description) AS (
  VALUES
    ('gastronomy.catalog.read', 'View the gastronomy menu'),
    ('gastronomy.catalog.write', 'Create and update menu items'),
    ('gastronomy.inventory.read', 'View ingredients and stock'),
    ('gastronomy.inventory.write', 'Manage ingredients and stock'),
    ('gastronomy.recipes.read', 'View recipes and costs'),
    ('gastronomy.recipes.write', 'Manage recipes and consumption'),
    ('gastronomy.orders.read', 'View tables, orders and kitchen status'),
    ('gastronomy.orders.write', 'Create and update orders'),
    ('gastronomy.orders.close', 'Close, cancel and settle orders')
)
INSERT INTO public.permissions(module_id, name, description)
SELECT m.id, d.name, d.description
FROM permission_definitions d
JOIN public.modules m ON m.code = 'gastronomy_tables'
ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description;

CREATE TABLE IF NOT EXISTS public.gastronomy_menu_items (
  product_id UUID PRIMARY KEY REFERENCES public.products(id) ON DELETE CASCADE,
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  category VARCHAR(100) NOT NULL,
  preparation_station VARCHAR(50) DEFAULT 'kitchen' NOT NULL,
  active BOOLEAN DEFAULT true NOT NULL,
  sort_order INTEGER DEFAULT 0 NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.gastronomy_ingredients (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  name VARCHAR(255) NOT NULL,
  unit VARCHAR(30) NOT NULL,
  current_stock NUMERIC(14, 3) DEFAULT 0 NOT NULL CHECK (current_stock >= 0),
  minimum_stock NUMERIC(14, 3) DEFAULT 0 NOT NULL CHECK (minimum_stock >= 0),
  cost_per_unit NUMERIC(14, 4) DEFAULT 0 NOT NULL CHECK (cost_per_unit >= 0),
  supplier_name VARCHAR(255),
  active BOOLEAN DEFAULT true NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, name)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_recipes (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE CASCADE NOT NULL,
  portions NUMERIC(12, 3) DEFAULT 1 NOT NULL CHECK (portions > 0),
  instructions TEXT,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, product_id)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_recipe_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  recipe_id UUID REFERENCES public.gastronomy_recipes(id) ON DELETE CASCADE NOT NULL,
  ingredient_id UUID REFERENCES public.gastronomy_ingredients(id) ON DELETE RESTRICT NOT NULL,
  quantity NUMERIC(14, 4) NOT NULL CHECK (quantity > 0),
  waste_percentage NUMERIC(5, 2) DEFAULT 0 NOT NULL CHECK (waste_percentage BETWEEN 0 AND 100),
  UNIQUE(recipe_id, ingredient_id)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_dining_areas (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE CASCADE NOT NULL,
  name VARCHAR(100) NOT NULL,
  sort_order INTEGER DEFAULT 0 NOT NULL,
  active BOOLEAN DEFAULT true NOT NULL,
  UNIQUE(branch_id, name)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_dining_tables (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE CASCADE NOT NULL,
  area_id UUID REFERENCES public.gastronomy_dining_areas(id) ON DELETE CASCADE NOT NULL,
  name VARCHAR(50) NOT NULL,
  capacity INTEGER DEFAULT 2 NOT NULL CHECK (capacity > 0),
  status VARCHAR(20) DEFAULT 'available' NOT NULL CHECK (status IN ('available', 'occupied', 'reserved', 'cleaning', 'blocked')),
  UNIQUE(branch_id, name)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_orders (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  table_id UUID REFERENCES public.gastronomy_dining_tables(id) ON DELETE SET NULL,
  user_id UUID NOT NULL,
  order_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  channel VARCHAR(20) DEFAULT 'dine_in' NOT NULL CHECK (channel IN ('dine_in', 'takeaway', 'delivery')),
  status VARCHAR(30) DEFAULT 'open' NOT NULL CHECK (status IN ('open', 'sent', 'preparing', 'ready', 'served', 'closed', 'cancelled')),
  customer_name VARCHAR(255),
  notes TEXT,
  subtotal NUMERIC(14, 2) DEFAULT 0 NOT NULL,
  total NUMERIC(14, 2) DEFAULT 0 NOT NULL,
  version INTEGER DEFAULT 1 NOT NULL,
  opened_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  closed_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS public.gastronomy_order_items (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  order_id UUID REFERENCES public.gastronomy_orders(id) ON DELETE CASCADE NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  quantity NUMERIC(12, 3) NOT NULL CHECK (quantity > 0),
  unit_price NUMERIC(14, 2) NOT NULL CHECK (unit_price >= 0),
  status VARCHAR(30) DEFAULT 'pending' NOT NULL CHECK (status IN ('pending', 'sent', 'preparing', 'ready', 'served', 'cancelled')),
  notes TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

