-- Supermarket domain: persistent catalog, purchasing and FEFO lot traceability.

WITH permission_definitions(name, description) AS (
  VALUES
    ('supermarket.catalog.read', 'View supermarket products and prices'),
    ('supermarket.catalog.write', 'Create and update supermarket products'),
    ('supermarket.inventory.read', 'View supermarket stock and lots'),
    ('supermarket.inventory.write', 'Adjust supermarket stock'),
    ('supermarket.purchases.read', 'View supermarket purchase orders'),
    ('supermarket.purchases.write', 'Create and update supermarket purchase orders'),
    ('supermarket.purchases.receive', 'Receive merchandise and create stock lots'),
    ('supermarket.sales.create', 'Commit supermarket sales and stock consumption')
)
INSERT INTO public.permissions(module_id, name, description)
SELECT m.id, d.name, d.description
FROM permission_definitions d
JOIN public.modules m ON m.code = 'barcode_scanner'
ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description;

CREATE TABLE IF NOT EXISTS public.supermarket_products (
  product_id UUID PRIMARY KEY REFERENCES public.products(id) ON DELETE CASCADE,
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  category VARCHAR(40) NOT NULL,
  unit VARCHAR(10) DEFAULT 'unit' NOT NULL CHECK (unit IN ('unit', 'kg')),
  is_weighed BOOLEAN DEFAULT false NOT NULL,
  promo VARCHAR(20) DEFAULT 'none' NOT NULL CHECK (promo IN ('none', '2x1', '30off')),
  supplier_name VARCHAR(255),
  active BOOLEAN DEFAULT true NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.supermarket_purchase_orders (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  order_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  supplier_name VARCHAR(255) NOT NULL,
  expected_on DATE,
  status VARCHAR(20) DEFAULT 'draft' NOT NULL CHECK (status IN ('draft', 'ordered', 'received', 'cancelled')),
  created_by UUID NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  received_at TIMESTAMP WITH TIME ZONE,
  UNIQUE(company_id, order_number)
);

CREATE TABLE IF NOT EXISTS public.supermarket_purchase_order_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  order_id UUID REFERENCES public.supermarket_purchase_orders(id) ON DELETE CASCADE NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  quantity NUMERIC(14, 3) NOT NULL CHECK (quantity > 0),
  unit_cost NUMERIC(14, 4) DEFAULT 0 NOT NULL CHECK (unit_cost >= 0),
  lot_code VARCHAR(100),
  expiration_date DATE,
  UNIQUE(order_id, product_id)
);

CREATE TABLE IF NOT EXISTS public.supermarket_stock_lots (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  purchase_order_line_id UUID REFERENCES public.supermarket_purchase_order_lines(id) ON DELETE SET NULL,
  lot_code VARCHAR(100) NOT NULL,
  quantity_received NUMERIC(14, 3) NOT NULL CHECK (quantity_received > 0),
  quantity_remaining NUMERIC(14, 3) NOT NULL CHECK (quantity_remaining >= 0),
  expiration_date DATE,
  received_on DATE DEFAULT CURRENT_DATE NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, branch_id, product_id, lot_code)
);

CREATE INDEX IF NOT EXISTS idx_supermarket_products_company
  ON public.supermarket_products(company_id, active, category, product_id);
CREATE INDEX IF NOT EXISTS idx_supermarket_purchases_branch_status
  ON public.supermarket_purchase_orders(company_id, branch_id, status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_supermarket_purchase_lines_order
  ON public.supermarket_purchase_order_lines(company_id, order_id, product_id);
CREATE INDEX IF NOT EXISTS idx_supermarket_lots_fefo
  ON public.supermarket_stock_lots(company_id, branch_id, product_id, expiration_date, quantity_remaining)
  WHERE quantity_remaining > 0;
