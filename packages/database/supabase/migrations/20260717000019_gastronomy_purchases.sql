CREATE INDEX IF NOT EXISTS idx_gastronomy_purchase_receipts_order
  ON public.gastronomy_purchase_receipts(company_id, branch_id, order_id, received_at DESC);
CREATE INDEX IF NOT EXISTS idx_gastronomy_purchase_receipt_lines_receipt
  ON public.gastronomy_purchase_receipt_lines(company_id, receipt_id);

