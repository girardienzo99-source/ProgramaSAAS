-- Gastronomy cash sessions, split payments, tips and transactional table settlement.

WITH permission_definitions(name, description) AS (
  VALUES
    ('gastronomy.cash.read', 'View gastronomy cash status and settlements'),
    ('gastronomy.cash.manage', 'Open and close gastronomy cash registers'),
    ('gastronomy.settlements.read', 'View gastronomy table settlements'),
    ('gastronomy.settlements.create', 'Settle gastronomy tables and register payments')
)
INSERT INTO public.permissions(module_id, name, description)
SELECT m.id, d.name, d.description
FROM permission_definitions d
JOIN public.modules m ON m.code = 'gastronomy_tables'
ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description;

CREATE TABLE IF NOT EXISTS public.gastronomy_settlements (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  caja_id UUID REFERENCES public.cajas(id) ON DELETE RESTRICT NOT NULL,
  sale_id UUID REFERENCES public.sales(id) ON DELETE RESTRICT NOT NULL,
  table_id UUID REFERENCES public.gastronomy_dining_tables(id) ON DELETE RESTRICT NOT NULL,
  user_id UUID NOT NULL,
  settlement_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  idempotency_key VARCHAR(128) NOT NULL,
  split_count INTEGER DEFAULT 1 NOT NULL CHECK (split_count BETWEEN 1 AND 20),
  subtotal NUMERIC(14, 2) NOT NULL CHECK (subtotal >= 0),
  tax_amount NUMERIC(14, 2) NOT NULL CHECK (tax_amount >= 0),
  sale_total NUMERIC(14, 2) NOT NULL CHECK (sale_total > 0),
  tip_amount NUMERIC(14, 2) DEFAULT 0 NOT NULL CHECK (tip_amount >= 0),
  charged_total NUMERIC(14, 2) NOT NULL CHECK (charged_total > 0),
  fiscal_status VARCHAR(30) DEFAULT 'pending' NOT NULL
    CHECK (fiscal_status IN ('pending', 'authorized', 'rejected', 'not_required')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, idempotency_key)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_settlement_payments (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  settlement_id UUID REFERENCES public.gastronomy_settlements(id) ON DELETE CASCADE NOT NULL,
  payment_number INTEGER NOT NULL CHECK (payment_number > 0),
  payment_method VARCHAR(20) NOT NULL CHECK (payment_method IN ('cash', 'card', 'qr')),
  amount NUMERIC(14, 2) NOT NULL CHECK (amount > 0),
  reference VARCHAR(100),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(settlement_id, payment_number)
);

CREATE INDEX IF NOT EXISTS idx_gastronomy_settlements_branch
  ON public.gastronomy_settlements(company_id, branch_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_gastronomy_settlements_cash
  ON public.gastronomy_settlements(company_id, caja_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_gastronomy_settlement_payments
  ON public.gastronomy_settlement_payments(company_id, settlement_id);
CREATE UNIQUE INDEX IF NOT EXISTS idx_gastronomy_one_open_cash_per_branch
  ON public.cajas(company_id, branch_id)
  WHERE status = 'open';

ALTER TABLE public.gastronomy_settlements ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.gastronomy_settlement_payments ENABLE ROW LEVEL SECURITY;

