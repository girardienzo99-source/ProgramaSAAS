-- Supplier discrepancy claims created from purchase receipts and exchanged through EDI COMDIS.

WITH permission_definitions(name, description) AS (
  VALUES
    ('supermarket.supplier_claims.read', 'View supermarket supplier discrepancy claims'),
    ('supermarket.supplier_claims.manage', 'Acknowledge and resolve supermarket supplier discrepancy claims')
)
INSERT INTO public.permissions(module_id, name, description)
SELECT m.id, d.name, d.description
FROM permission_definitions d
JOIN public.modules m ON m.code = 'barcode_scanner'
ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description;

CREATE TABLE IF NOT EXISTS public.supermarket_supplier_claims (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  supplier_id UUID REFERENCES public.supermarket_suppliers(id) ON DELETE SET NULL,
  supplier_name VARCHAR(255) NOT NULL,
  order_id UUID REFERENCES public.supermarket_purchase_orders(id) ON DELETE CASCADE NOT NULL,
  receipt_id UUID REFERENCES public.supermarket_purchase_receipts(id) ON DELETE CASCADE NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  claim_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  claim_type VARCHAR(30) NOT NULL
    CHECK (claim_type IN ('partial_delivery', 'rejected_items', 'document_mismatch', 'missing_asn')),
  status VARCHAR(20) DEFAULT 'open' NOT NULL
    CHECK (status IN ('open', 'acknowledged', 'disputed', 'resolved')),
  priority VARCHAR(10) DEFAULT 'normal' NOT NULL CHECK (priority IN ('normal', 'urgent')),
  subject VARCHAR(180) NOT NULL,
  description TEXT NOT NULL,
  claimed_quantity NUMERIC(14, 3) NOT NULL CHECK (claimed_quantity >= 0),
  response_due_on DATE NOT NULL,
  resolution_notes TEXT,
  opened_by UUID NOT NULL,
  acknowledged_at TIMESTAMP WITH TIME ZONE,
  resolved_at TIMESTAMP WITH TIME ZONE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, claim_number),
  UNIQUE(company_id, receipt_id)
);

CREATE TABLE IF NOT EXISTS public.supermarket_supplier_claim_events (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  claim_id UUID REFERENCES public.supermarket_supplier_claims(id) ON DELETE CASCADE NOT NULL,
  actor_type VARCHAR(20) NOT NULL CHECK (actor_type IN ('system', 'internal', 'supplier')),
  actor_id UUID,
  idempotency_key VARCHAR(128) NOT NULL,
  from_status VARCHAR(20),
  to_status VARCHAR(20) NOT NULL CHECK (to_status IN ('open', 'acknowledged', 'disputed', 'resolved')),
  notes TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, idempotency_key)
);

CREATE INDEX IF NOT EXISTS idx_supermarket_supplier_claims_queue
