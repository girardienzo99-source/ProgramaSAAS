-- Supermarket inventory controls: counts, waste, branch transfers and bulk pricing.

WITH permission_definitions(name, description) AS (
  VALUES
    ('supermarket.inventory.count', 'Register supermarket cycle counts'),
    ('supermarket.inventory.waste', 'Register supermarket waste'),
    ('supermarket.inventory.transfer', 'Transfer supermarket stock between branches'),
    ('supermarket.prices.bulk', 'Apply supermarket bulk price changes')
)
INSERT INTO public.permissions(module_id, name, description)
SELECT m.id, d.name, d.description
FROM permission_definitions d
JOIN public.modules m ON m.code = 'barcode_scanner'
ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description;

CREATE TABLE IF NOT EXISTS public.supermarket_inventory_events (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  user_id UUID NOT NULL,
  idempotency_key VARCHAR(128) NOT NULL,
  operation VARCHAR(20) NOT NULL CHECK (operation IN ('count', 'waste')),
  previous_quantity NUMERIC(14, 3) NOT NULL CHECK (previous_quantity >= 0),
  declared_quantity NUMERIC(14, 3) NOT NULL CHECK (declared_quantity >= 0),
  delta NUMERIC(14, 3) NOT NULL,
  reason VARCHAR(160) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, idempotency_key)
);

CREATE TABLE IF NOT EXISTS public.supermarket_transfers (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  source_branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  destination_branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  user_id UUID NOT NULL,
  transfer_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  idempotency_key VARCHAR(128) NOT NULL,
  status VARCHAR(20) DEFAULT 'completed' NOT NULL CHECK (status IN ('completed', 'cancelled')),
  notes VARCHAR(200),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  CHECK (source_branch_id <> destination_branch_id),
  UNIQUE(company_id, idempotency_key),
  UNIQUE(company_id, transfer_number)
);

CREATE TABLE IF NOT EXISTS public.supermarket_transfer_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  transfer_id UUID REFERENCES public.supermarket_transfers(id) ON DELETE CASCADE NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  quantity NUMERIC(14, 3) NOT NULL CHECK (quantity > 0),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(transfer_id, product_id)
);

CREATE TABLE IF NOT EXISTS public.supermarket_price_batches (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  user_id UUID NOT NULL,
  idempotency_key VARCHAR(128) NOT NULL,
  description VARCHAR(160) NOT NULL,
  updated_count INTEGER DEFAULT 0 NOT NULL CHECK (updated_count >= 0),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, idempotency_key)
);

CREATE TABLE IF NOT EXISTS public.supermarket_price_batch_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  batch_id UUID REFERENCES public.supermarket_price_batches(id) ON DELETE CASCADE NOT NULL,
  product_id UUID REFERENCES public.products(id) ON DELETE RESTRICT NOT NULL,
  previous_price NUMERIC(14, 2) NOT NULL CHECK (previous_price >= 0),
  new_price NUMERIC(14, 2) NOT NULL CHECK (new_price >= 0),
  previous_promo VARCHAR(20) NOT NULL,
  new_promo VARCHAR(20) NOT NULL,
  UNIQUE(batch_id, product_id)
);

CREATE INDEX IF NOT EXISTS idx_supermarket_inventory_events_branch
