  VALUES
    ('supermarket.purchase_receipts.read', 'View partial supermarket purchase receipts'),
    ('supermarket.purchase_receipts.create', 'Create partial supermarket purchase receipts'),
    ('supermarket.edi.read', 'View queued supermarket EDI events')
