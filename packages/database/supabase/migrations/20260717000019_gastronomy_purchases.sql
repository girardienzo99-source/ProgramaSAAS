-- Gastronomy suppliers, purchase orders and transactional stock receipts.

WITH permission_definitions(name, description) AS (
  VALUES
    ('gastronomy.suppliers.read', 'View gastronomy suppliers'),
    ('gastronomy.suppliers.write', 'Create and update gastronomy suppliers'),
    ('gastronomy.purchases.read', 'View gastronomy purchase orders and receipts'),
    ('gastronomy.purchases.write', 'Create and update gastronomy purchase orders'),
    ('gastronomy.purchases.receive', 'Receive gastronomy purchases into inventory')
)
INSERT INTO public.permissions(module_id, name, description)
SELECT m.id, d.name, d.description
FROM permission_definitions d
JOIN public.modules m ON m.code = 'gastronomy_tables'
ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description;

CREATE TABLE IF NOT EXISTS public.gastronomy_suppliers (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  name VARCHAR(255) NOT NULL,
  tax_id VARCHAR(20),
  phone VARCHAR(50),
  email VARCHAR(255),
  address TEXT,
  active BOOLEAN DEFAULT true NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(company_id, name)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_gastronomy_suppliers_tax_id
  ON public.gastronomy_suppliers(company_id, tax_id)
  WHERE tax_id IS NOT NULL AND tax_id <> '';

CREATE TABLE IF NOT EXISTS public.gastronomy_purchase_orders (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  supplier_id UUID REFERENCES public.gastronomy_suppliers(id) ON DELETE RESTRICT NOT NULL,
  user_id UUID NOT NULL,
  order_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  status VARCHAR(30) DEFAULT 'draft' NOT NULL
    CHECK (status IN ('draft', 'ordered', 'partially_received', 'received', 'cancelled')),
  expected_at DATE,
  notes TEXT,
  total_estimated NUMERIC(14, 2) DEFAULT 0 NOT NULL CHECK (total_estimated >= 0),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  ordered_at TIMESTAMP WITH TIME ZONE,
  received_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS public.gastronomy_purchase_order_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  order_id UUID REFERENCES public.gastronomy_purchase_orders(id) ON DELETE CASCADE NOT NULL,
  ingredient_id UUID REFERENCES public.gastronomy_ingredients(id) ON DELETE RESTRICT NOT NULL,
  quantity_ordered NUMERIC(14, 3) NOT NULL CHECK (quantity_ordered > 0),
  quantity_received NUMERIC(14, 3) DEFAULT 0 NOT NULL
    CHECK (quantity_received >= 0 AND quantity_received <= quantity_ordered),
  unit_cost NUMERIC(14, 4) NOT NULL CHECK (unit_cost >= 0),
  UNIQUE(order_id, ingredient_id)
);

CREATE TABLE IF NOT EXISTS public.gastronomy_purchase_receipts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  branch_id UUID REFERENCES public.branches(id) ON DELETE RESTRICT NOT NULL,
  order_id UUID REFERENCES public.gastronomy_purchase_orders(id) ON DELETE RESTRICT NOT NULL,
  user_id UUID NOT NULL,
  receipt_number BIGINT GENERATED BY DEFAULT AS IDENTITY,
  notes TEXT,
  received_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.gastronomy_purchase_receipt_lines (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE NOT NULL,
  receipt_id UUID REFERENCES public.gastronomy_purchase_receipts(id) ON DELETE CASCADE NOT NULL,
  order_line_id UUID REFERENCES public.gastronomy_purchase_order_lines(id) ON DELETE RESTRICT NOT NULL,
  ingredient_id UUID REFERENCES public.gastronomy_ingredients(id) ON DELETE RESTRICT NOT NULL,
  quantity NUMERIC(14, 3) NOT NULL CHECK (quantity > 0),
  unit_cost NUMERIC(14, 4) NOT NULL CHECK (unit_cost >= 0)
);

CREATE INDEX IF NOT EXISTS idx_gastronomy_suppliers_company
  ON public.gastronomy_suppliers(company_id, active, name);
CREATE INDEX IF NOT EXISTS idx_gastronomy_purchase_orders_branch
  ON public.gastronomy_purchase_orders(company_id, branch_id, status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_gastronomy_purchase_lines_order
